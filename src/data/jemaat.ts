  // ── Core (Phase 1) ──────────────────────────────────────────────────────────
  id: string;
  nama: string;        // Full church name
  pendeta: string;     // Lead pastor / pelayan
  alamat: string;      // Full address
  telepon: string;     // Contact number
  lat: number;         // Latitude
  lng: number;         // Longitude
